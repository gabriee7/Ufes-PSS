public class Cliente {
    private String nome;
    private String tipo; //GOLD, SILVER, DIAMOND
    
    public Cliente(String nome, String tipo){
        this.nome = nome;
        this.tipo = tipo;
    }

    public String getNome() {
        return nome;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
}
